export type LanguageCode = (typeof languageOptions)[number]["code"];
export type TranslationKey = string;

export const languageStorageKey = "councilpoint_language";

export function getStoredLanguage(): LanguageCode {
  if (typeof window === "undefined") {
    return "en";
  }

  const stored = window.localStorage.getItem(languageStorageKey);
  return isLanguageCode(stored) ? stored : "en";
}

export function getLanguageOption(code: LanguageCode) {
  return languageOptions.find((language) => language.code === code) ?? languageOptions[0];
}

export function translate(code: LanguageCode, key: TranslationKey) {
  return translations[code]?.[key] ?? translations.en[key];
}

function isLanguageCode(value: string | null): value is LanguageCode {
  return languageOptions.some((language) => language.code === value);
}

